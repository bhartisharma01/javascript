// slab move
var canvas=document.getElementById('gameBoardArea');
	var ctx=canvas.getContext('2d');
	const image=document.getElementById('source');
	const player={
		w:170,
		h:11,
		x:0,
		y:200,
		speed:5,
		dx:0,
		dy:0
	}
	function drawPlayer(){
		ctx.drawImage(image, player.x,player.y,player.w,player.h);

	}
	function clear(){
		ctx.clearRect(0,0,canvas.width,canvas.height);
	}
	function newPos(){
		player.x+=player.dx;
		player.y+=player.dy;
		detectWalls();
	}
	function detectWalls(){
		// left wall
		if(player.x<0){
			player.x=0;
		}
		// right wall
		if(player.x+player.w>canvas.width){
			player.x=canvas.width-player.w;
		}
		
		if(player.y+player.h > canvas.height){
			player.y=canvas.height-player.h;
		}

	}
	function update(){
		clear();
		drawPlayer();
		newPos();
		requestAnimationFrame(update);
	}
	
	function moveLeft(){
		player.dx=-player.speed;
	}
	function moveRight(){
		player.dx=player.speed;
	}
	function keyDown(e){
		if(e.key==='ArrowRight' || 
			e.key==='Right'){
			moveRight();
		} 
			if(e.key==='ArrowLeft' || 
			e.key==='Left'){
				moveLeft();
			} 
	}
	function keyUp(e){
		if(e.key=='Right'||
			e.key=='ArrowRight'||
			e.key=='Left'||
			e.key=='ArrowLeft'){
			player.dx=0;
		player.dy=0;
		}
	}
	update();
	document.addEventListener('keydown',keyDown);document.addEventListener('keyup',keyUp);


